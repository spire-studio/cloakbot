#!/usr/bin/env bash
# One-command demo launcher for the Gemma 4 Good Hackathon.
#
# This script:
#   1. Starts Ollama with Gemma 4 E2B (pulls the model on first run)
#   2. Bootstraps a minimal .env pointing at the local Ollama endpoint
#   3. Launches the CloakBot WebUI (gateway on :8000, frontend on :5173)
#   4. Opens http://127.0.0.1:5173 in your browser
#
# Once it's up, drag docs/demo/demo_onboarding_memo.md into the Composer:
#   - 20 PII entities detected by the local Gemma 4 E2B kernel
#   - chunker-backed redaction (2 chunks at the default 6,000-char boundary)
#   - Click "Diff" to see the Local vs Remote side-by-side
#
# Prerequisites:
#   - Ollama installed:  curl -fsSL https://ollama.com/install.sh | sh
#   - uv installed:      curl -LsSf https://astral.sh/uv/install.sh | sh
#   - Python deps:       uv sync
#   - Frontend deps:     cd webui && npm install   (~349 MB node_modules)
#
# Env overrides:
#   OLLAMA_MODEL    Default: gemma4:e2b  (passed through to start_ollama.sh)
#   GATEWAY_PORT    Default: 8000        (WebUI gateway port)
#   FRONTEND_PORT   Default: 5173        (Vite dev port)
#   SKIP_OPEN       Set to 1 to skip auto-opening the browser

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
cd "$REPO_ROOT"

GATEWAY_PORT="${GATEWAY_PORT:-8000}"
FRONTEND_PORT="${FRONTEND_PORT:-5173}"
OLLAMA_HOST="${OLLAMA_HOST:-127.0.0.1:11434}"
OLLAMA_MODEL="${OLLAMA_MODEL:-gemma4:e2b}"

# Step 1 — local backend
echo "==> [1/3] Starting Ollama backend (model: ${OLLAMA_MODEL})"
OLLAMA_MODEL="$OLLAMA_MODEL" OLLAMA_HOST="$OLLAMA_HOST" \
    bash "$REPO_ROOT/scripts/start_ollama.sh"

# Step 2 — bootstrap .env if missing
if [[ ! -f .env ]]; then
    echo "==> [2/3] Creating .env pointed at local Ollama"
    cat > .env <<EOF
# Auto-generated by scripts/quickstart_demo.sh
# CloakBot uses one OpenAI-compatible client for both vLLM and Ollama;
# the VLLM_* variable names are historical.
VLLM_BASE_URL=http://${OLLAMA_HOST}/v1
VLLM_API_KEY=ollama
VLLM_MODEL=${OLLAMA_MODEL}
EOF
else
    echo "==> [2/3] .env already exists — leaving it alone"
    echo "    (If you want the demo to use Ollama, ensure VLLM_BASE_URL"
    echo "     points at http://${OLLAMA_HOST}/v1)"
fi

# Step 3 — WebUI (gateway + frontend; cloakbot CLI starts both)
echo "==> [3/3] Starting WebUI"
echo "    Gateway : http://127.0.0.1:${GATEWAY_PORT}"
echo "    Frontend: http://127.0.0.1:${FRONTEND_PORT}"
echo ""

# Open browser after a short delay so the frontend has time to come up
if [[ "${SKIP_OPEN:-0}" != "1" ]]; then
    (
        sleep 6
        url="http://127.0.0.1:${FRONTEND_PORT}"
        if command -v open >/dev/null 2>&1; then
            open "$url"
        elif command -v xdg-open >/dev/null 2>&1; then
            xdg-open "$url"
        fi
    ) &
fi

cat <<EOF

╭───────────────────────────────────────────────────────────────╮
│  Demo flow                                                    │
│                                                               │
│  1. The browser opens to http://127.0.0.1:${FRONTEND_PORT}                │
│  2. Drag docs/demo/demo_onboarding_memo.md into the Composer  │
│  3. Ask "Summarize this memo for the leadership team"         │
│  4. Click 'Diff' on either bubble to see Local vs Remote      │
│                                                               │
│  Press Ctrl-C to stop the WebUI. Ollama keeps running in the  │
│  background; stop it with: pkill ollama                       │
╰───────────────────────────────────────────────────────────────╯

EOF

exec uv run python -m cloakbot webui \
    --port "$GATEWAY_PORT" \
    --frontend-port "$FRONTEND_PORT"
